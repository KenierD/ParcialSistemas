    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        try {
            if (btnConectar.getText().equalsIgnoreCase("Conectar")) {
                puerto = Integer.parseInt(txtPuerto.getText());
                ipservidor = txtIP.getText();
                invocadorRemoto = new CallHandler();
                cliente = new Client(ipservidor, puerto, invocadorRemoto);
                calculoRemoto = (ICalculoRemotoImc) cliente.getGlobal(ICalculoRemotoImc.class);
                btnConectar.setText("Desconectar");
                btnConectar.setForeground(Color.white);
                btnConectar.setBackground(Color.red);
                lblEstado.setText("Conectado");
                lblEstado.setForeground(Color.GREEN);
            } else if (btnConectar.getText().equals("Desconectar")) {
                cliente.close();
                btnConectar.setText("Conectar");
                btnConectar.setForeground(Color.WHITE);
                btnConectar.setBackground(Color.GREEN);
                lblEstado.setText("Desconectado");
                lblEstado.setForeground(Color.RED);
            }
        } catch (Exception e) {
            System.out.println("Error al conectar");
        }
    }//GEN-LAST:event_btnConectarActionPerformed

    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        float peso = Float.parseFloat(txtPeso.getText());
        float altura = Float.parseFloat(txtAltura.getText());
        Thread hilo = new Thread() {
            @Override
            public void run() {
                try {
                    System.out.println("Peso: " + peso);
                    System.out.println("Altura: " + altura);
                    DatosImc datos = new DatosImc();
                    datos.setAltura(altura);
                    datos.setPeso(peso);
                    System.out.println("Transportando datos...\nEspere un momento....");
                    datos = calculoRemoto.calcularImc(datos);
                    System.out.println("IMC: " + datos.getResultado()
                        + "\nMensaje: " + datos.getInterpretación());
                    resultMensaje.setText(datos.getInterpretación());
                    resultIMC.setText(datos.getResultado()+"");
                } catch (Exception e) {
                    JOptionPane.showConfirmDialog(VentanaPrincipal.this, "Error en el cliente de la aplicación");
                    System.out.println("Error con el cliente " + e.getMessage());
                    e.printStackTrace();
                }
            }
        };
        hilo.start();
    }//GEN-LAST:event_btnCalcularActionPerformed

